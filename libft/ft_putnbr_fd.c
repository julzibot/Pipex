#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		mod;
	char	c;

	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	else if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		mod = (n % 10);
		n /= 10;
		c = mod + '0';
		ft_putnbr_fd(n, fd);
		write (fd, &c, 1);
	}
	else if (n < 10 && n >= 0)
	{
		c = n + '0';
		write (fd, &c, 1);
	}
}
