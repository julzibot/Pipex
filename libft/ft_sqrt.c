#include "libft.h"

int	ft_sqrt(int nb)
{
	unsigned int	a;

	if (nb <= 0 || nb == 2)
		return (0);
	a = 0;
	while (a * a < (unsigned) nb)
		a++;
	if (nb % a == 0)
		return ((int) a);
	return (0);
}
